package ListNode_Practice;

/**
 * @author Will$
 * @version 1.0
 * @description: Definition for singly-linked list.
 * @date 2022-04-20$ $
 */
public class ListNode {
    int val;
    ListNode next;
    ListNode() {}
    ListNode(int val) { this.val = val; }
    ListNode(int val, ListNode next) { this.val = val; this.next = next; }
}